class TeacherInputCapture:
    def __init__(self, teacher_model):
        self.teacher_model = teacher_model
        self.inputs_dict = {}
        self.outputs_dict = {}
        self._register_hooks()

    def _clone_tensor_tree(self, value):
        if value is None:
            return None
        if isinstance(value, torch.Tensor):
            return value.detach().clone().contiguous()
        if isinstance(value, tuple):
            return tuple(self._clone_tensor_tree(item) for item in value)
        if isinstance(value, list):
            return [self._clone_tensor_tree(item) for item in value]
        return value

    def _create_layer_hook(self, layer_idx):
        def hook(module, args, kwargs):
            hook_inputs = {
                'hidden_states': self._clone_tensor_tree(args[0]),
                'attention_mask': self._clone_tensor_tree(kwargs.get('attention_mask')),
                'position_ids': self._clone_tensor_tree(kwargs.get('position_ids')),
                'past_key_value': None,
                'use_cache': False,   #'output_attentions': kwargs.get('output_attentions'),
                'cache_position': self._clone_tensor_tree(kwargs.get('cache_position')),
                'position_embeddings': self._clone_tensor_tree(kwargs.get('position_embeddings')),
            }

            self.inputs_dict[layer_idx] = hook_inputs
        
        return hook

    def _create_layer_output_hook(self, layer_idx):
        def hook(module, args, kwargs, output):
            layer_output = output if isinstance(output, torch.Tensor) else output[0]
            self.outputs_dict[layer_idx] = self._clone_tensor_tree(layer_output)

        return hook

    def _register_hooks(self):
        for idx, layer in enumerate(self.teacher_model.base_model.language_model.layers):  # 注意模型实际路径
            layer.register_forward_pre_hook(self._create_layer_hook(idx), with_kwargs=True)
            layer.register_forward_hook(self._create_layer_output_hook(idx), with_kwargs=True)
    
    def clear(self):
        self.inputs_dict.clear()
        self.outputs_dict.clear()


class LayerDistillationTrainer(Seq2SeqTrainer):
    """Stage 1: Layer-wise MSE distillation between student and teacher hidden states."""

    def __init__(
        self,
        teacher_model,
        finetuning_args: "FinetuningArguments",
        processor: Optional["ProcessorMixin"],
        gen_kwargs: Optional[dict[str, Any]] = None,
        **kwargs,
    ) -> None:
        if is_transformers_version_greater_than("4.46"):
            kwargs["processing_class"] = kwargs.pop("tokenizer")
        else:
            self.processing_class: PreTrainedTokenizer = kwargs.get("tokenizer")

        self.teacher_model = teacher_model
        self.teacher_device = next(self.teacher_model.parameters()).device
        self.teacher_capture = TeacherInputCapture(teacher_model)

        super().__init__(**kwargs)
        if processor is not None:
            self.model_accepts_loss_kwargs = False

        self.finetuning_args = finetuning_args
        if gen_kwargs is not None:
            self._gen_kwargs = gen_kwargs

        if processor is not None:
            self.add_callback(SaveProcessorCallback(processor))

        if finetuning_args.use_badam:
            from badam import BAdamCallback, clip_grad_norm_old_version  # type: ignore

            self.accelerator.clip_grad_norm_ = MethodType(clip_grad_norm_old_version, self.accelerator)
            self.add_callback(BAdamCallback)

    @override
    def compute_loss(self, model, inputs, *args, **kwargs):
        self.teacher_capture.clear()
        with torch.no_grad():
            self.teacher_model(**inputs, output_hidden_states=False)
        
        for idx, layer in enumerate(model.module.base_model.language_model.layers):
            teacher_layer_target = self.teacher_capture.outputs_dict[idx]
            
            x = layer(
                self.teacher_capture.inputs_dict[idx]["hidden_states"],
                attention_mask = self.teacher_capture.inputs_dict[idx]["attention_mask"],
                position_ids = self.teacher_capture.inputs_dict[idx]["position_ids"],
                past_key_values = self.teacher_capture.inputs_dict[idx]["past_key_value"],
                cache_position = self.teacher_capture.inputs_dict[idx]["cache_position"],
                position_embeddings = self.teacher_capture.inputs_dict[idx]["position_embeddings"]
            )[0]
            
            layer_loss = F.mse_loss(x, teacher_layer_target)
            
            if idx == 0:
                total_loss = layer_loss
            else:
                total_loss += layer_loss
    
        
        total_loss = total_loss / len(model.module.base_model.language_model.layers) * self.finetuning_args.mse_factor
        
        return total_loss

    @override
    def create_optimizer(self) -> "torch.optim.Optimizer":
        if self.optimizer is None:
            self.optimizer = create_custom_optimizer(self.model, self.args, self.finetuning_args)
        return super().create_optimizer()

    @override
    def create_scheduler(
        self, num_training_steps: int, optimizer: Optional["torch.optim.Optimizer"] = None
    ) -> "torch.optim.lr_scheduler.LRScheduler":
        create_custom_scheduler(self.args, num_training_steps, optimizer)
        return super().create_scheduler(num_training_steps, optimizer)

    @override
    def _get_train_sampler(self, *args, **kwargs) -> Optional["torch.utils.data.Sampler"]:
        if self.finetuning_args.disable_shuffling:
            return torch.utils.data.SequentialSampler(self.train_dataset)

        return super()._get_train_sampler(*args, **kwargs)

    @override
    def prediction_step(
        self,
        model: "torch.nn.Module",
        inputs: dict[str, Union["torch.Tensor", Any]],
        prediction_loss_only: bool,
        ignore_keys: Optional[list[str]] = None,
        **gen_kwargs,
    ) -> tuple[Optional[float], Optional["torch.Tensor"], Optional["torch.Tensor"]]:
        r"""Remove the prompt part in the generated tokens."""

        if self.args.predict_with_generate:
            labels = inputs.pop("labels", None)
        else:
            labels = inputs.get("labels")

        loss, generated_tokens, _ = super().prediction_step(
            model, inputs, prediction_loss_only=prediction_loss_only, ignore_keys=ignore_keys, **gen_kwargs
        )
        if generated_tokens is not None and self.args.predict_with_generate:
            generated_tokens[:, : inputs["input_ids"].size(-1)] = self.processing_class.pad_token_id
            generated_tokens = generated_tokens.contiguous()

        return loss, generated_tokens, labels

    def save_predictions(
        self, dataset: "Dataset", predict_results: "PredictionOutput", skip_special_tokens: bool = True
    ) -> None:
        r"""Save model predictions to `output_dir`."""

        if not self.is_world_process_zero():
            return

        output_prediction_file = os.path.join(self.args.output_dir, "generated_predictions.jsonl")
        logger.info_rank0(f"Saving prediction results to {output_prediction_file}")

        labels = np.where(
            predict_results.label_ids != IGNORE_INDEX, predict_results.label_ids, self.processing_class.pad_token_id
        )
        preds = np.where(
            predict_results.predictions != IGNORE_INDEX,
            predict_results.predictions,
            self.processing_class.pad_token_id,
        )

        for i in range(len(preds)):
            pad_len = np.nonzero(preds[i] != self.processing_class.pad_token_id)[0]
            if len(pad_len):
                preds[i] = np.concatenate((preds[i][pad_len[0] :], preds[i][: pad_len[0]]), axis=-1)

        decoded_inputs = self.processing_class.batch_decode(dataset["input_ids"], skip_special_tokens=False)
        decoded_preds = self.processing_class.batch_decode(preds, skip_special_tokens=skip_special_tokens)
        decoded_labels = self.processing_class.batch_decode(labels, skip_special_tokens=skip_special_tokens)

        with open(output_prediction_file, "w", encoding="utf-8") as f:
            for text, pred, label in zip(decoded_inputs, decoded_preds, decoded_labels):
                f.write(json.dumps({"prompt": text, "predict": pred, "label": label}, ensure_ascii=False) + "\n")


class EndtoEndDistillationTrainer(Seq2SeqTrainer):
    """Stage 2: End-to-end distillation with KL divergence + layer MSE loss."""

    def __init__(
        self,
        teacher_model,
        finetuning_args: "FinetuningArguments",
        processor: Optional["ProcessorMixin"],
        gen_kwargs: Optional[dict[str, Any]] = None,
        **kwargs,
    ) -> None:
        if is_transformers_version_greater_than("4.46"):
            kwargs["processing_class"] = kwargs.pop("tokenizer")
        else:
            self.processing_class: PreTrainedTokenizer = kwargs.get("tokenizer")

        self.teacher_model = teacher_model
        self.teacher_device = next(self.teacher_model.parameters()).device
        self.teacher_capture = TeacherInputCapture(teacher_model)

        super().__init__(**kwargs)
        if processor is not None:
            self.model_accepts_loss_kwargs = False

        self.finetuning_args = finetuning_args
        if gen_kwargs is not None:
            self._gen_kwargs = gen_kwargs

        if processor is not None:
            self.add_callback(SaveProcessorCallback(processor))

        if finetuning_args.use_badam:
            from badam import BAdamCallback, clip_grad_norm_old_version  # type: ignore

            self.accelerator.clip_grad_norm_ = MethodType(clip_grad_norm_old_version, self.accelerator)
            self.add_callback(BAdamCallback)

        self.kl_div = nn.KLDivLoss(reduction="batchmean")

    @override
    def compute_loss(self, model, inputs, *args, **kwargs):
        student_outputs = model(**inputs, output_hidden_states=True)

        # 运行教师模型
        with torch.no_grad():
            teacher_outputs = self.teacher_model(**inputs, output_hidden_states=True)

        #layer_losses = []
        #for s, t in zip(student_outputs.hidden_states[1:-1], teacher_outputs.hidden_states[1:-1]):
        #    layer_losses.append(F.mse_loss(s.float(), t.float()))
        #layer_loss = sum(layer_losses) / len(layer_losses) 

        student_logits = student_outputs.get('logits').float()
        teacher_logits = teacher_outputs.get('logits').float()

        teacher_probs = torch.nn.functional.softmax(teacher_logits / self.finetuning_args.distill_temperature, dim=-1)
        student_probs = torch.nn.functional.log_softmax(student_logits / self.finetuning_args.distill_temperature , dim=-1)
        
        # KL divergence loss
        kl_loss = self.kl_div(student_probs, teacher_probs) * (self.finetuning_args.distill_temperature ** 2)

        total_loss = kl_loss

        return total_loss


    @override
    def create_optimizer(self) -> "torch.optim.Optimizer":
        if self.optimizer is None:
            self.optimizer = create_custom_optimizer(self.model, self.args, self.finetuning_args)
        return super().create_optimizer()

